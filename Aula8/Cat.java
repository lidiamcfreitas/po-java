public class Cat{
    private int _age;
    private double _weight;
    private String _name;

    public Cat(int age, double weight, String name)
    {
        _age = age;
        _weight = weight;
        _name = name;
    }

    public int getAge() { return _age; }

    public double getWeight() { return _weight; }

    public String getName() { return _name; }
    

}
